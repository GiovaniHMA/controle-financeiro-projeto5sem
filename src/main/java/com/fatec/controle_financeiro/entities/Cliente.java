package com.fatec.controle_financeiro.entities;

public class Cliente {

    private int id;
    private String name;

    public Cliente(int id, String name){
        this.id = id;
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
        
    }

}

